# Take two integer inputs from the user
a = int(input("Enter a value: "))  # Example: 10
b = int(input("Enter b value: "))  # Example: 3

# Perform and display various arithmetic operations

# Addition
print("Addition =", a + b)  # Adds a and b => 10 + 3 = 13

# Subtraction
print("Subtraction =", a - b)  # Subtracts b from a => 10 - 3 = 7

# Multiplication
print("Multiplication =", a * b)  # Multiplies a and b => 10 * 3 = 30

# Division (returns float)
print("Division =", a / b)  # Divides a by b => 10 / 3 = 3.333...

# Modulus (remainder)
print("Modulus =", a % b)  # Remainder when a is divided by b => 10 % 3 = 1

# Exponentiation
print("Exponent =", a ** b)  # a raised to the power of b => 10^3 = 1000

# Floor Division (integer division)
print("Floor Division =", a // b)  # Integer part of division => 10 // 3 = 3
