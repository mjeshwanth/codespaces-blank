# Take input from the user and convert it to integers
a = int(input("Enter a value: "))  # Example: 10
b = int(input("Enter b value: "))  # Example: 3

# Perform and display various arithmetic operations

# Addition
print("Addition =", a + b)  # 10 + 3 = 13

# Subtraction
print("Subtraction =", a - b)  # 10 - 3 = 7

# Multiplication
print("Multiplication =", a * b)  # 10 * 3 = 30

# Division (returns float result)
print("Division =", a / b)  # 10 / 3 = 3.333...

# Modulus (remainder)
print("Modulus =", a % b)  # 10 % 3 = 1

# Exponentiation (a raised to the power of b)
print("Exponent =", a ** b)  # 10 ** 3 = 1000

# Floor Division (division without decimal part)
print("Floor Division =", a // b)  # 10 // 3 = 3
